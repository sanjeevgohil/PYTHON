# Mathemetical function

import numpy as np

a = np.array([10, 20, 30])
b = np.array([1, 2, 3])

print("Addition of A & B is :", np.add(a, b))
print("Subtraction of A & B is :", np.subtract(a, b))
print("Multiplication of A & B is :", np.multiply(a, b))
print("Division of A & B is :", np.divide(a, b))
print("Power of A & B is :", np.power(a, b))